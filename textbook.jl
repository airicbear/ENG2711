**2.4** Films produce meaning by making reference to history, to real-world locations, objects, or people, to other art forms, and to other films.

**2.5** Film style emerges from the interaction of a film’s formal mode of organization, its subject matter, and its visual and sound elements.
Films fall into three main categories of style, defined by specific traits: classical (invested in clear storytelling), realist (interested in exploring characters and capturing life), and formalist (overtly interventionist work that calls attention to the process of representation).

